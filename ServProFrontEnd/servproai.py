"""**Reasoning**:
I will define the `recommend_service` function which incorporates preprocessing, vectorization, cosine similarity for content-based recommendations, and intent prediction using the trained model and label encoder, then test it with the provided sample queries to demonstrate its functionality.


"""

def recommend_service(query_text):
    # Preprocess the query text
    preprocessed_query = preprocess_text(query_text)

    # Vectorize the preprocessed query
    query_vector = tfidf_vectorizer.transform([preprocessed_query])

    # Calculate cosine similarity with service descriptions
    similarity_scores = cosine_similarity(query_vector, service_description_features)

    # Get the index of the most similar service based on content
    content_best_match_idx = np.argmax(similarity_scores)
    content_best_service = services_df.loc[content_best_match_idx, 'Service Name']
    content_confidence = similarity_scores[0, content_best_match_idx]

    # Predict intent using the trained model
    predicted_intent_label = intent_model.predict(query_vector)
    predicted_intent_service = label_encoder.inverse_transform(predicted_intent_label)[0]

    # Combine recommendations (for this initial implementation, we'll favor content similarity
    # but note the predicted intent for further refinement)
    if content_best_service == predicted_intent_service:
        recommended_service = content_best_service
        confidence = content_confidence
    else:
        # If content and intent disagree, we could implement more complex logic.
        # For now, we'll lean on the content-based match and acknowledge the predicted intent.
        recommended_service = content_best_service
        confidence = content_confidence

    return recommended_service, confidence, predicted_intent_service

