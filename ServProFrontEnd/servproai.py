def generate_response(query_text, recommended_service, confidence, predicted_intent_service):
    response = f"Great news! I found a highly recommended service for you: **{recommended_service}**.\n"
    gemini_alternative_response = None

    # Define thresholds for low confidence or unclear intent
    low_confidence_threshold = 0.1 # Using 0.1 as an example threshold
    is_low_confidence = confidence <= low_confidence_threshold
    is_unclear_intent = recommended_service != predicted_intent_service

    if is_low_confidence or is_unclear_intent:
        response += (f"While I'm suggesting **{recommended_service}**, my primary system indicates this with a confidence of {confidence:.2f}.\n")
        response += (f"And my intent recognition suggests **{predicted_intent_service}**.\n")
        response += ("To ensure we're on the right track, I've consulted with an advanced AI assistant to provide an alternative perspective.\n")

        try:
            # Call Gemini API for alternative recommendation
            # Ensure system_instruction and gemini_model are accessible in this scope
            gemini_response_obj = gemini_model.generate_content(
                system_instruction + "\n\nUser query: " + query_text
            )
            gemini_alternative_response = gemini_response_obj.text
            response += f"\nHere's what our advanced AI suggests based on your query: {gemini_alternative_response}\n"
            response += ("We're dedicated to finding the perfect match for your needs!\n")

        except Exception as e:
            response += f"However, I encountered an issue fetching an alternative recommendation from our advanced AI: {e}. I apologize for the inconvenience.\n"
            response += "Please bear with us as we improve our systems.\n"

    else:
        response += f"I'm quite confident about this recommendation (Confidence: {confidence:.2f}).\n"

    response += "We're here to ensure you get the best assistance possible. How else can I help you today?"
    return response

